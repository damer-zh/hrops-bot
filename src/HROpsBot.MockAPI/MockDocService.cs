    public Task<List<Regulation>> SearchAsync(string query)
    {
        if (string.IsNullOrWhiteSpace(query)) return Task.FromResult(_regulations);

        var q = query.ToLower();
        var results = _regulations.Where(r =>
            r.TitleRu.ToLower().Contains(q) ||
            r.TitleKk.ToLower().Contains(q) ||
            r.ContentRu.ToLower().Contains(q) ||
            r.ContentKk.ToLower().Contains(q) ||
            r.Tags.ToLower().Contains(q) ||
            r.Category.ToLower().Contains(q)
        ).ToList();

        return Task.FromResult(results);
    }

    public Task<Regulation?> GetByIdAsync(int id) =>
        Task.FromResult(_regulations.FirstOrDefault(r => r.Id == id));
